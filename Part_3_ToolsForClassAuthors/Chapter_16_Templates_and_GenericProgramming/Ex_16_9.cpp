/************************************************************************************************************************************************************
 * function template : 
 *      Function template is a generic defintion from which type specific function can be instantiated by
 * deducing the types of the template parameters from the arguments passed to the function call.
 * 
 * class template : 
 *      class template is a blueprint(generic definition) from which classes can be generated. But unlike function templates
 * template parameters will not be deduced by the compiler, we have to explicitly pass the template argument in angle
 * brackets following the class template name.   
 * 
 *************************************************************************************************************************************************************/
